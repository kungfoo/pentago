	//GEN-FIRST:event_save_buttonActionPerformed
	private void save_buttonActionPerformed(java.awt.event.ActionEvent evt) {
		Theme thm = (Theme)theme_combobox.getSelectedItem();
		UserConfigManager.setUserConfigItem("client", "theme", thm.getName());
		UserConfigManager.setUserConfigItem("client", "color", (String)color_combobox.getSelectedItem());
	
		ImageLoader.dropCache();
		if(ClientState.currentGame != null) {
			ClientState.currentGame.repaint();
		}
		this.dispose();

	}//GEN-LAST:event_save_buttonActionPerformed

