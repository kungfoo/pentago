	private void setup() {
		String usertheme = UserConfigManager.getUserConfigItem("client",
				"theme");
		List<Theme> list = ThemeConfigManager.getThemeList();
		ListCellRenderer theme_renderer = new ThemeListCellRenderer();
		theme_combobox.setRenderer(theme_renderer);
		DefaultComboBoxModel theme_model = (DefaultComboBoxModel) theme_combobox.getModel();
		for (Theme theme : list) {
			theme_model.addElement(theme);
			if (theme.getName().equals(usertheme)) {
				theme_combobox.setSelectedItem(theme);
			}
		}
		ListCellRenderer color_renderer = new ColorListCellRenderer(theme_combobox);
		color_combobox.setRenderer(color_renderer);
		DefaultComboBoxModel color_model = (DefaultComboBoxModel) color_combobox.getModel();
		
		color_model.addElement("white");
		color_model.addElement("black");
		color_combobox.setSelectedItem(UserConfigManager.getUserConfigItem("client", "color"));
		
	}

